                String date = jLabel4.getText();
                 String balance = jLabel5.getText();
                  String amount = txtAmount.getText();

            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost/snehbank","root","");
            //--Deposite Insert query
            insert = conn.prepareStatement("insert into deposit(acc_id,cust_id,date,balance,deposit)values(?,?,?,?,?)");   
            
             insert.setString(1, accno);
            insert.setString(2, cust_id);
            insert.setString(3, date);
            insert.setString(4, balance);
            insert.setString(5, amount);
            
           insert.executeUpdate();
           
           
           
            //--Deposite Update query
           
           update = conn.prepareStatement("update account set balance= balance+ ? where acc_id=?");
           update.setString(1, amount);
           update.setString(2, accno);
           
           update.executeUpdate();
           
